
public class EvenNumber {

	public static void main(String[] args) {
		
		
		
		int number=100;
		
		for(int i=1;i<=number;i++){
			
			 if( i%2==0){
				 
				System.out.println("Even--"+i);
				
			 }else if( i%2==1){
				 
				 System.out.println("Odd---"+i);  
				 
			 }
			
		}
		
	}

}
